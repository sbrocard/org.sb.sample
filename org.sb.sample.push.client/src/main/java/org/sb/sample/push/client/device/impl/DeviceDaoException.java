package org.sb.sample.push.client.device.impl;

public class DeviceDaoException extends Exception {

	public DeviceDaoException() {
	}

	public DeviceDaoException(String message) {
		super(message);
	}

	public DeviceDaoException(Throwable cause) {
		super(cause);
	}

	public DeviceDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public DeviceDaoException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
